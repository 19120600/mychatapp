    public static List<MyAcc> updateOnl() throws IOException{
        List<MyAcc> list = new ArrayList<MyAcc>();
        BufferedReader file = new BufferedReader(new FileReader("src/mychatapp/myaccOnl.txt"));
        int n = Integer.parseInt(file.readLine());
        for (int i = 0; i < n; i++)
        {
            String std = file.readLine();
            String[] in4 = std.split(";");
            MyAcc acc = new MyAcc(in4[0], in4[1], in4[2]);
            list.add(acc);
        }
        file.close();
        return list;
    }
