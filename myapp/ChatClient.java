    private void btnChatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChatActionPerformed
        // TODO add your handling code here:
        
        try{
            this.listOnl = updateOnl();
            String port = null;
            MyAcc desAcc = null;
            for (MyAcc a: this.list){
                if (a.getUser().equals(friendChat.getText())){
                    port = a.getPort();
                    desAcc = a;
                    break;
                }
            }
            System.out.println(port);
            int dem = 0;
            for (MyAcc a: this.listOnl){
                if (desAcc.getPort().equals(a.getPort())){
                    dem = dem + 1;
                    break;}
            }
            if (this.acc.getPort().equals(port)){
                JOptionPane.showMessageDialog(this, "You can't chat with yourself!");
            }
            else if (dem==0){
                JOptionPane.showMessageDialog(this, "Your friend is not online!");
                }
            else {  
                ChatClientForm fr = new ChatClientForm(this.acc, desAcc);
                fr.setVisible(true);
                dispose();
            }

        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Err");
        }
    }//GEN-LAST:event_btnChatActionPerformed

